  abstract: 'Plants coordinate the expression of genes required to conduct photosynthesis in response to growth and environmental changes. In species that conduct two-cell C4 photosynthesis, the expression of photosynthesis genes is partitioned such that leaf mesophyll and vascular sheath cells accumulate different components of the photosynthetic pathway. The identity of the regulatory networks that facilitate this partitioning are unknown. Here we show that differences in light perception between mesophyll and bundle sheath cells facilitate differential regulation and accumulation of photosynthesis genes in the C4 crop Zea mays (maize). We show that transcripts encoding photoreceptors differentially accumulate in mesophyll and bundle sheath cells in a manner that is consistent with biophysical light filtration. We further show the blue light (but not red) is necessary and sufficient to activate photosystem II assembly in etiolated maize mesophyll cells, while both red and blue produce the same effect in C3 Hordeum vulgare (barley). Finally, we demonstrate that changes in abundance of >20% of genes that differentially accumulate between mesophyll and bundle sheath cells can be recapitulated by spectrum specific de-etiolation of maize seedlings. These findings provide evidence that subdivision of light signalling networks is a key component of cellular partitioning of C4 photosynthesis in maize.',
